#include "3-calc.h"
/**
 *op_add- add two numbers together
 *@a:first number
 *@b:second number
 *Return:int
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 *op_sub- subtracts two numbers
 *@a:first number
 *@b:second number
 *Return:int
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 *op_mul- multiplies two numbers together
 *@a:first number
 *@b:second number
 *Return:int
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 *op_div- divide two numbers by each other
 *@a:first number
 *@b:second number
 *Return:int
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 *op_mod- return remainder of two numbers
 *@a:first number
 *@b:second number
 *Return:int
 */
int op_mod(int a, int b)
{
return (a % b);
}
